/*
 * Callback function on receiving message from USB.
 */
void USB_CDC_RxHandler(uint8_t* Buf, uint32_t Len)
{
	const char target[] = "CALC";	// string to read from message for calculations

	/* Check whether the command has CALC. */
	if (memcmp(Buf, target, strlen(target)) == 0)
	{
		// Do calculation
		Buf[Len] = '\0'; // Ensure the buffer is null-terminated -> use it as string
		do_calc((char*)Buf);	// Call function to do calculation
	}
	else
	{
		CDC_Transmit_FS((uint8_t*)"No CALC command received.\r\n", USB_TX_BUFFER);
	}
}

/*
 * Function to do calculation.
 */
void do_calc(char* command)
{
	HAL_GPIO_TogglePin(LED_Blue_GPIO_Port, LED_Blue_Pin);

	char* token;
	char response[USB_TX_BUFFER];
	int result = 0;

	token = strtok(command, " ");	// Read first string i.e. CALC -> Not needed
	token = strtok(NULL, " ");		// Read second string of command

	if (strcmp(token, "SUM") == 0)
	{
		HAL_GPIO_TogglePin(LED_Orange_GPIO_Port, LED_Orange_Pin);

		result = 0;
		while((token = strtok(NULL, " ")) != NULL)
		{
			result += atoi(token);
		}

		// Send back answer
		sprintf(Usb_Tx_Buffer_Calc_Ans, "Summed number: %d\r\n", result);
		CDC_Transmit_FS(Usb_Tx_Buffer_Calc_Ans, USB_TX_BUFFER);
	}
	if (strcmp(token, "SUB") == 0)
	{
		HAL_GPIO_TogglePin(LED_Red_GPIO_Port, LED_Red_Pin);

		result = 0;
		token = strtok(NULL, " "); // Get the first number
		result = atoi(token);
		while((token = strtok(NULL, " ")) != NULL)
		{
			result -= atoi(token);
		}

		// Send back answer
		sprintf(Usb_Tx_Buffer_Calc_Ans, "Subtracted number: %d\r\n", result);
		CDC_Transmit_FS(Usb_Tx_Buffer_Calc_Ans, USB_TX_BUFFER);
	}
}
