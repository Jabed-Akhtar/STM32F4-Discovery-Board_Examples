void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	HAL_StatusTypeDef iic_tx_status;
	uint8_t uart_msg_tx_status_ok[] = "Msg sent through IIC successfully - ONCE.\r\n";
	uint8_t uart_msg_tx_status_nok[] = "Error sending through IIC - ONCE.\r\n";
	uint8_t Val_Tmp[1] = {0x0};
	Val_Tmp[0] = TxData_Once[0];

	// Call the function and get the returned array
	for (int i=0; i<sizeof(TxData_Once)-1; i++)
	{
		TxData_Once[i] = TxData_Once[i+1];
	}
	TxData_Once[sizeof(TxData_Once)-1] = Val_Tmp[0];

	iic_tx_status = HAL_I2C_Master_Transmit_DMA(&hi2c1, IIC_Slave_ADDR, TxData_Once, 6); //Sending in DMA mode
//	iic_status = HAL_I2C_IsDeviceReady(&hi2c1, 0x14, 10, 100);

	if (iic_tx_status==HAL_OK)
	{
		HAL_UART_Transmit(&huart2, uart_msg_tx_status_ok, sizeof(uart_msg_tx_status_ok), 100);
		HAL_GPIO_TogglePin(GPIOD, LD6_Pin); // Blue
	}
	else if (iic_tx_status==HAL_ERROR)
	{
		HAL_UART_Transmit(&huart2, uart_msg_tx_status_nok, sizeof(uart_msg_tx_status_nok), 100);
		HAL_GPIO_TogglePin(GPIOD, LD5_Pin); // Red
	}
}

